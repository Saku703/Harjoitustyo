package com.example.myapplication.ui.addFood;

public class Protein {
    int protein_beef;
    int protein_fish;
    int protein_pork;
    int protein_poultry;

    public Protein() {
        protein_beef = 0;
        protein_fish = 0;
        protein_pork = 0;
        protein_poultry = 0;
    }

    public int getProtein_beef() {
        return protein_beef;
    }

    public int getProtein_fish() {
        return protein_fish;
    }

    public int getProtein_pork() {
        return protein_pork;
    }

    public int getProtein_poultry() {
        return protein_poultry;
    }
}
